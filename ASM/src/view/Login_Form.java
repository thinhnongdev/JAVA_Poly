    private void btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoginActionPerformed
        // TODO add your handling code here:
        String user = txt_User.getText();
        String pass = new String(txt_PassWord.getPassword());
//         for (Login login : ss.dangNhap(user, pass)) {
//            if (login.getUser().equals(user)==false || login.getPassword().equals(pass)==false) {
//                
//                JOptionPane.showMessageDialog(this, "Login Fail");
//        }
//         }
        boolean flag = false;
        for (Login login : ss.dangNhap(user, pass)) {
            if (login.getUser().equals(user) && login.getPassword().equals(pass)) {
                flag = true;
                JOptionPane.showMessageDialog(this, "Login Succesfully");
                if (login.getRole().equals("admin")) {
                    QuanLySinhVien_Form qlsv = new QuanLySinhVien_Form();
                    qlsv.setVisible(true);
                    dispose();
                    JOptionPane.showMessageDialog(this, "Form quản lý sinh viên đang mở");
                } else {
                    QuanLyDiemSV_Form qldsv = new QuanLyDiemSV_Form();
                    qldsv.setVisible(true);
                    dispose();
                    JOptionPane.showMessageDialog(this, "Form quản lý điểm sinh viên đang mở");
                }
            }
        }

        if (!flag) {
            JOptionPane.showMessageDialog(this, "Login fail");
        }


    }//GEN-LAST:event_btn_LoginActionPerformed

